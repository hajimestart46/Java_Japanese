
public class learn {
	public static void main(String[] args) {
		
		//format　フォーマット　形式
				//クラスやインタフェース：大文字は1文字目
				//変数やメソッドやパッケージ：小文字は1文字目
				
				/*  変数名のルール
				    - 1文字目
				    英字(a-z,A-Z), _ , S
				    - 2文字目以降は数字も可
				    - キーワードは使えない
				*/
				int $a = 1;//int型は$aを宣言し、1を代入した
				//初期化する
				System.out.println($a);//$できる
				int _a = 1;
				System.out.println(_a);//_できる
				//_aを参照する
			    
			    float a = 11f;
			    System.out.println(a);
		
	}
}
